        <h2>The development of Blockchain</h2>
        <p>The aim of blockchain is to allow for the recording and distribution of digital data without the ability to edit it. Stuart Haber and W. Scott Stornetta, two academics who sought to develop a system where record timestamps could not be tampered with, first outlined blockchain technology in 1991. But it wasn't until January 2009, almost two decades later, that blockchain received its first real-world use, with the introduction of Bitcoin. 
        <br><br>
        A blockchain is the foundation of the Bitcoin protocol. Bitcoin's pseudonymous inventor, Satoshi Nakamoto, described the digital currency as a new electronic cash system that’s fully peer-to-peer, with no trusted third party”  in a research paper introducing it<sup id="cite_ref2" class="reference"><a href="#cite_note2">[2]</a></sup></p>
