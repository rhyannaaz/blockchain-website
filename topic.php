    <section id="basics">
        <h2>What is Blockchain ?</h2>
        <p><strong>Blockchain defined:</strong> In a business network, blockchain is a decentralized, immutable ledger that makes it easier to record transactions and monitor properties. A tangible asset (a home, car, cash, or land) may be intangible (intellectual property, patents, copyrights, branding). On a blockchain network, virtually anything of value can be tracked and exchanged, lowering risk and cutting costs for all parties involved.
        <br><br>
        <strong>Why blockchain is important:</strong> Business relies heavily on information. It's preferable if it's received quickly and accurately. Since it offers instant, decentralised, and fully transparent information stored on an immutable database that can only be accessed by permissioned network participants, blockchain is perfect for delivering that information. Orders, transfers, accounts, production, and much more can all be tracked using a blockchain network.<sup id="cite_ref1" class="reference"><a href="#cite_note1">[1]</a></sup></p>
